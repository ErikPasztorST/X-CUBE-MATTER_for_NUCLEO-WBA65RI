uint8_t FACTORYDATA_Write(void) {

	uint8_t err = DATAFACTORY_DATA_NOT_FOUND;
	uint8_t *Location = Factory_Data_buff;

	 //search for keyword location
	Location = Get_MagicKeyWordLocation();
	if (Location == Factory_Data_buff) {
		return err;
	} else {
		// Write Certificates in ram buffer
		STM_ecdsa_keypair STM_Keypair;
		STM_DAC_cert STM_dac;
		STM_PAI_cert STM_pai;
		uint8_t STM_provision_mode = MODE;

		memcpy(STM_Keypair.priv_key,kDevelopmentDAC_PrivateKey_FFF1_8004,32);
		memcpy(STM_Keypair.pub_key,kDevelopmentDAC_PublicKey_FFF1_8004,65);
		STM_Keypair.priv_key_len = 32;
		STM_Keypair.pub_key_len = 65;

		memcpy(STM_dac.val,&kDevelopmentDAC_Cert_FFF1_8004,493);
		STM_dac.len = 493;

		memcpy(STM_pai.val,&kDevelopmentPAI_Cert_FFF1,463);
		STM_pai.len = 463;

		err = Set_Certificates(Location, &STM_Keypair, &STM_dac, &STM_pai, &STM_provision_mode);
		if (err == DATAFACTORY_OK) {
			// delete old factory data
			STM_EXT_FLASH_Delete_Image(FACTORY_DATA_START_ADDR,
			FACTORY_DATA_LENGTH);
			//bump ram buffer in factory data location
			err = STM_EXT_FLASH_WriteChunk(FACTORY_DATA_START_ADDR,
					Factory_Data_buff, FACTORY_DATA_LENGTH);
		}
	}

	return err;
}

